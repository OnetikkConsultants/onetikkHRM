using System;
using System.Collections.Generic;
using System.Linq;
using System.Text.RegularExpressions;
using System.Threading.Tasks;

namespace HRM.Core
{
    public static class StringExtensions
    {

        public static int CompareIgnoreCase(this string source, string target)
        {
            return string.Compare(source, target, true);
        }

        public static string Encrypt(this string str)
        {
            return Encryption.Encrypt(str);
        }

        public static string Decrypt(this string str)
        {
            return Encryption.Decrypt(str);
        }

        public static string Hash(this string str, string salt)
        {
            return Hashing.GenerateHash(str, salt);
        }

        public static bool IsValidEmailAddress(this string str)
        {
            return (emailAddressRegEx.Match(str).Length > 0);
        }

        // NOTE: Email validation regular expression - identical to the one used by [EmailAddressAttribute] 
